// Math Operations
console.log(Math.sqrt(16)); //finding square root - 4
console.log(Math.pow(2, 3)); //exponentiation - 8
console.log(Math.abs(-10)); //absolute value - 10
console.log(Math.max(2, 5, 1)); //maximum value - 5
console.log(Math.min(2, 5, 1)); //minimum value - 1
console.log(Math.round(11.34)); //rounding - 11
console.log(Math.floor(11.99)); //floor value - 11
console.log(Math.ceil(11.01)); //ceiling value - 12
console.log(Math.trunc(11.99)); //truncation - 11
console.log(Math.random()); //random number - varies
console.log(Math.sign(-5)); //sign - -1

